package com.gamelab.mmi;

public class EnemyParameters {

	public final int enemy;
	public final float enemySpawn;
	public final float enemyDespawn;
	
	public EnemyParameters(final int enemy, final float enemySpawn, final float enemyDespawn) {
		this.enemy = enemy;
		this.enemySpawn = enemySpawn;
		this.enemyDespawn = enemyDespawn;
	}
	
	
}
